	function createQuestionElement(index) {
		var qElement = $('<div>', {
			id: 'question'
		});
		
		var question = $('<p><span style =" font-weight: bold;">Question ' + (index + 1) + ': </span>').append(questionsArray[index].question);
		qElement.append(question);
    
		var radioButtons = createRadios(index);
		qElement.append(radioButtons);
		qElement.append('<br>');

		return qElement;
	}
