	// Creates a list of the answer choices as radio inputs
	function createRadios(index) {
		var radioList = $('<ul>');
		var item;
		var input = '';
		for (var i = 0; i < questionsArray[index].choice.length; i++) {
			item = $('<li>');
			input = '<input type="radio" name="answer" value=' + i + ' />'+"&nbsp ";
			input +=questionsArray[index].choice[i];
			
			item.append(input);
			radioList.append(item);
		}
		return radioList;
	}
    // Reads the user selection and pushes the value to an array
